int			ft_iterative_power(int nb, int power)
{
	int a;
	int i;

	a = 1;
	i = 0;
	if (power < 0)
	{
		return (0);
	}
	while (i < power)
	{
		a = a * nb;
		i++;
	}
	return (a);
}
