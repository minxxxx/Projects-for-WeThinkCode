#include "BaseObject.h"

///////////////////////////////////////////////////////////////////
// constructor
//
///////////////////////////////////////////////////////////////////
BaseObject::BaseObject(int id, int type) {

	this->_transformable = true;

	this->_selectable = true;

	this->_type = type;

	this->_id = id;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
int BaseObject::getType() {

	return _type;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
int BaseObject::getId() {

	return _id;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
Vector* BaseObject::getPosition() {

	return &_position;
}

void BaseObject::setPosition(double x, double y, double z) {

	_position.set(x, y, z);
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
Vector* BaseObject::getOffset() {

	return &_offset;
}

void BaseObject::setOffset(double x, double y, double z) {

	_offset.set(x, y, z);
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
bool BaseObject::getTransformable() {

  return _transformable;
}

void BaseObject::setTransformable(bool transformable) {

  _transformable = transformable;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
bool BaseObject::getSelectable() {

  return _selectable;
}

void BaseObject::setSelectable(bool selectable) {

  _selectable = selectable;
}
