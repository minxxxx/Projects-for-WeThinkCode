#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	size_t	a;
	size_t	b;

	b = 0;
	a = ft_strlen(s1);
	while (b < ft_strlen(s2))
	{
		s1[a] = s2[b];
		a++;
		b++;
	}
	s1[a] = '\0';
	return (s1);
}
