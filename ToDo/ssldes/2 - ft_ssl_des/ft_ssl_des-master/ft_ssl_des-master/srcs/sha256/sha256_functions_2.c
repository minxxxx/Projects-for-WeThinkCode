#include <ft_ssl.h>

uint32_t	ps0(uint32_t x)
{
	return (rotr(7, x) ^ rotr(18, x) ^ (x >> 3));
}

uint32_t	ps1(uint32_t x)
{
	return (rotr(17, x) ^ rotr(19, x) ^ (x >> 10));
}
