#include "libft.h"

int	ft_isspace(int c)
{
	unsigned char ch;

	ch = (unsigned char)c;
	return (ch == ' ' || ch == '\t' || ch == '\v' || ch == '\n' || ch == '\t'\
		|| ch == '\f' || ch == '\r');
}
