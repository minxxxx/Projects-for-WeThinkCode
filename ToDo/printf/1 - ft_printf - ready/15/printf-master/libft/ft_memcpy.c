#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*dptr;
	unsigned char	*sptr;

	dptr = (unsigned char *)dst;
	sptr = (unsigned char *)src;
	while (n)
	{
		*dptr = *sptr;
		++dptr;
		++sptr;
		--n;
	}
	return (dst);
}
