#include "../inc/game_2048.h"

int			ft_diff_save(t_map *map)
{
	int y;
	int x;

	y = -1;
	while (++y < 4)
	{
		x = -1;
		while (++x < 4)
			if (map->tab[y][x] != map->save[y][x])
				return (0);
	}
	return (1);
}
