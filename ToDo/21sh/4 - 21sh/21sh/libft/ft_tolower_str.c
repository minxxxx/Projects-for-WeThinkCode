#include "libft.h"

void	ft_tolower_str(char **str)
{
	size_t		i;

	if (*str == NULL)
		return ;
	i = 0;
	while ((*str)[i])
	{
		(*str)[i] = ft_tolower((*str)[i]);
		i++;
	}
}
