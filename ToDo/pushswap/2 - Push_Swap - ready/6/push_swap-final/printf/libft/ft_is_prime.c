#include "libft.h"

int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 2)
		return (0);
	while (nb % i != 0 && i < nb)
		i++;
	if (nb == i)
		return (1);
	else
		return (0);
	return (1);
}
