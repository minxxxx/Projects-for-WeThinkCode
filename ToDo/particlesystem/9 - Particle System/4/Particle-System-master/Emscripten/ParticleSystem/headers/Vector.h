	///////////////////////////////////////////////////////////////////
	//
	//
	///////////////////////////////////////////////////////////////////
	Vector asUnitVector() const;

	///////////////////////////////////////////////////////////////////
	//
	//
	///////////////////////////////////////////////////////////////////
