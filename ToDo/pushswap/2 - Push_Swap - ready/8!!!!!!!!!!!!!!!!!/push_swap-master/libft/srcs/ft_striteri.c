#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	if (!s || !f)
		return ;
	i = 0;
	if (s == NULL)
		return ;
	while (*s)
	{
		f(i, s);
		s++;
		i++;
	}
}
