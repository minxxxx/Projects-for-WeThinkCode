#include "../inc/game_2048.h"

void				init_map(t_map *map)
{
	int		i;
	int		j;

	j = 0;
	while (j <= 3)
	{
		i = 0;
		while (i <= 3)
		{
			map->tab[j][i] = 0;
			i++;
		}
		j++;
	}
}
