#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t s;
	size_t i;

	s = 0;
	i = 0;
	while (s1[s])
		s++;
	while (s2[i] && i < n)
	{
		s1[s + i] = s2[i];
		i++;
	}
	s1[s + i] = '\0';
	return (s1);
}
