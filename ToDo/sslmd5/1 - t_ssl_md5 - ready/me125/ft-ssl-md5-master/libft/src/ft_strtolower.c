#include "libft.h"

unsigned char	*ft_strtolower(unsigned char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if ((s[i] >= 'A') && (s[i] <= 'Z'))
			s[i] += 32;
		i++;
	}
	return (s);
}
