#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (n == 0)
		return (1);
	while (((int)*s1 == (int)*s2) && n--)
	{
		if ((*s1 == '\0') || (n == 0))
			return (1);
		s1++;
		s2++;
	}
	return (0);
}
