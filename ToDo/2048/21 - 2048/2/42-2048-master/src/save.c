#include "../inc/game_2048.h"

void		ft_save(t_map *map)
{
	int	y;
	int x;

	y = -1;
	while (++y < 4)
	{
		x = -1;
		while (++x < 4)
			map->save[y][x] = map->tab[y][x];
	}
}
