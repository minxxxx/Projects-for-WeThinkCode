int		ft_atoi(const char *str)
{
	int		neg;
	int		num;
	int		i;

	neg = 1;
	i = 0;
	if (str[0] == '-')
	{
		neg = -1;
		i++;
	}
	if (str[0] == '-' && (str[1] > 57 || str[1] < 48))
		return (0);
	while (str[i] > 57 || str[i] < 48)
		i++;
	num = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + str[i] - '0';
		i++;
	}
	return (neg * num);
}
