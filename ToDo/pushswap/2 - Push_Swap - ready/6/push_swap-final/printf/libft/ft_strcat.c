#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int t;

	i = ft_strlen(s1);
	t = 0;
	while (s2[t])
		s1[i++] = s2[t++];
	s1[i] = '\0';
	return (s1);
}
