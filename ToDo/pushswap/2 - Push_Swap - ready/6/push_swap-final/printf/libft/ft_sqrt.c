#include "libft.h"

int	ft_sqrt(int nb)
{
	int x;

	x = 1;
	if (nb == 1 || nb == 0)
		return (nb);
	if (nb < 0)
		return (0);
	while (nb != x * x && x < nb)
		x++;
	if (nb % x == 0 && nb != x)
		return (x);
	else
		return (0);
	return (0);
}
