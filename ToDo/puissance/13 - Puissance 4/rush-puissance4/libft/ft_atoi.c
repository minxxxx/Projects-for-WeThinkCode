#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		nb;
	int		sign;

	i = 0;
	nb = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	if (str[i] == '+' && sign != -1)
		i++;
	while (ft_isdigit(str[i]) == 1)
	{
		nb = (10 * nb) + str[i] - '0';
		i++;
	}
	return (sign * nb);
}
