int		is_conv(const char c)
{
	return (c == 'c' || c == 's' || c == 'd' || c == 'x' || c == 'p'
			|| c == 'o' || c == 'u' || c == 'S' || c == 'D' || c == 'i'
			|| c == 'O' || c == 'U' || c == 'X' || c == 'C' || c == '%');
}

int		is_flag(const char c)
{
	return (c == '#' || c == '0' || c == '-' || c == '+' || c == ' ');
}

int		is_lm(const char c)
{
	return (c == 'l' || c == 'h' || c == 'j' || c == 'z');
}
