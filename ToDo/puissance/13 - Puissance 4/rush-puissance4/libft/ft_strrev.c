#include "libft.h"

char	*ft_strrev(char *str)
{
	size_t	i;
	size_t	length;
	char	tmp;

	i = 0;
	length = ft_strlen(str);
	while (i < length / 2)
	{
		tmp = str[i];
		str[i] = str[length - i - 1];
		str[length - i - 1] = tmp;
		i++;
	}
	return (str);
}
